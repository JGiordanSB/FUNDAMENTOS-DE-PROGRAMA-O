#include <iostream>
using namespace std;
int main(){
    int jog1 = 0, jog2 = 0, jog3 = 0, jog4 = 0;
    cin >> jog1 >> jog2 >> jog3 >> jog4;
    int soma = jog1 + jog2 + jog3 + jog4;
    
    if(soma == 0) {
        cout << "nenhum\n";
    } else if(soma == 1 || soma % 4 == 1){
        cout << "jog1\n";
    } else if(soma == 2 || soma % 4 == 2){
        cout << "jog2\n";
    } else if(soma == 3 || soma % 4 == 3){
        cout << "jog3\n";
    } else{
        cout << "jog4\n";
    }

}
